import { appSettings } from './settings';
import { derived, get } from 'svelte/store';

type Locale = 'en' | 'pt' | 'ar';

const supportedLocales: Locale[] = ['en', 'pt', 'ar'];

export function getLocale() {
	const settings = get(appSettings);
	const locale = settings?.locale ?? 'en';
	return (supportedLocales.includes(locale as Locale) ? locale : 'en') as Locale;
}

export const t = derived(appSettings, () => {
	return <K extends keyof typeof translations>(key: K | undefined, params: Record<string, string> = {}): string => {
		const locale = getLocale();
		if (!key) {
			return '';
		}
		const translationEntry = translations[key];
		// Try the requested locale first, then fall back to English
		const template = (translationEntry as Record<string, string>)[locale] ?? translationEntry.en;

		if (!template) {
			console.warn(`Translation not found for key: ${String(key)} in locale: ${locale}`);
			return String(key);
		}

		return template.replace(/{{(.*?)}}/g, (_match: string, param: string) => {
			return params[param.trim()] ?? '';
		});
	};
});

